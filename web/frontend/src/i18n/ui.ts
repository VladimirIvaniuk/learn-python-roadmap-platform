export function getStoredUiLanguage(): UiLanguage {
  if (typeof window === 'undefined') return DEFAULT_UI_LANGUAGE
  const raw = localStorage.getItem(UI_LANGUAGE_STORAGE_KEY)
  return raw === 'en' || raw === 'uk' ? raw : DEFAULT_UI_LANGUAGE
}

export function setStoredUiLanguage(lang: UiLanguage) {
  if (typeof window === 'undefined') return
  localStorage.setItem(UI_LANGUAGE_STORAGE_KEY, lang)
}
