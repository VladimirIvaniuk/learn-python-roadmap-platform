# ── print() з різними параметрами ─────────────────────────────────────────────
print("Hello, World!")
print("Привіт!", "Python", "🐍")            # кілька аргументів
print("a", "b", "c", sep=" → ")            # 2026-03-01
print("2026", "03", "01", sep="-")          # власний sep
print("Рядок 1", end=" | ")                # без \n в кінці
print("Рядок 2")

# ── Змінні ────────────────────────────────────────────────────────────────────
name = "Дмитро"
age = 28
city = "Київ"
height = 1.80
is_student = False

print(name)
print(f"Вік: {age}")
print(f"Місто: {city}")

# Обмін значеннями (Python-стиль)
a, b = 10, 20
print(f"До: a={a}, b={b}")
a, b = b, a
print(f"Після: a={a}, b={b}")

# ── F-рядки ───────────────────────────────────────────────────────────────────
balance = 1_234_567.89

print(f"Привіт, {name}! Тобі {age} років, ти з {city}.")
print(f"Через 10 років тобі буде {age + 10}")
print(f"Ім'я великими: {name.upper()}")
print(f"Ти студент? {'Так' if is_student else 'Ні'}")
print(f"Баланс: {balance:,.2f} грн")        # 1,234,567.89 грн
print(f"Висота: {height:.0%}")              # як відсоток (відносно 1)
print(f"{age = }")                          # age = 28  (налагодження)

